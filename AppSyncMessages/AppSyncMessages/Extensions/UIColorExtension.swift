import UIKit

extension UIColor {
    static let reconRed: UIColor = {
        return UIColor(red: 0.58, green: 0.07, blue: 0.07, alpha: 1)
    }()
    
    static let reconGreen: UIColor = {
        return UIColor(red: 0.01, green: 0.87, blue: 0, alpha: 1)
    }()
    
    static let reconDarkGray: UIColor = {
        UIColor(red: 0.16, green: 0.16, blue: 0.16, alpha: 1)
    }()
    
    static let reconDarkBackground: UIColor = {
        UIColor(red: 0.12, green: 0.12, blue: 0.12, alpha: 1)
    }()
    
    
}
